// 알파벳 순으로 정렬된 국가 목록 (셀렉트박스에 사용)
export const SORTED_COUNTRIES = [...COUNTRIES].sort((a, b) =>
  a.name.localeCompare(b.name)
)

export function getLevelInfo(level: number) {
  return TRAVEL_LEVELS.find(l => l.level === level) || TRAVEL_LEVELS[0]
}

export function getCountryByCode(code: string) {
  return COUNTRIES.find(c => c.code === code)
}

/** 검색어에 매칭되는 국가 코드 목록 반환 (국가명·코드 부분 일치, 대소문자 무시) */
export function getCountryCodesMatchingQuery(searchQuery: string): string[] {
  const q = searchQuery.trim().toLowerCase()
  if (!q) return []
  return COUNTRIES.filter(
    c =>
      c.name.toLowerCase().includes(q) ||
      c.code.toLowerCase() === q
  ).map(c => c.code)
}

/** 2자리 국가 코드를 플래그 이모지로 변환 (목록에 없는 코드도 처리) */
export function getCountryFlagEmoji(code: string | null | undefined): string {
  if (!code || code.length !== 2) return '🏳'
  const base = 127397
  return String.fromCodePoint(...[...code.toUpperCase()].map(c => base + c.charCodeAt(0)))
}
